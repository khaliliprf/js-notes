// DOUBLE BANG !!
// + casting to NUMBER
// !! casting to BOOLEAN
// if we want to save a non boolean as a boolean we should convert and then save it
// into a variable
// if we use unary ! (means single !) it flip the sign (because it is not operator)
console.log(12); // 12
console.log(!12); // false
console.log(!!12); // true
